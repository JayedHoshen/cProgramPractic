#include <stdio.h>

int main () {

    // Float - %f
    float f = 489.123F;
    printf("Float - %f\n", f);

    // Double - %lf
    double d = 477789.56;
    printf("Double - %lf\n", d);

    // Long double - %Lf
    long double ld = 78545542.565;
    printf("Long Double - %Lf\n", ld);

    return 0;
}